function set_all_types(property_name)
{
    
    let ret = json_array.reduce(function(acc,object)
    {
      let array = object[property_name];
      if(typeof(array) != typeof([]))
      {
        array = [array];
      }
      array.forEach(function(item)
      {
        if(!acc.includes(item))
        {
          acc.unshift(item);
        }
      });
      return acc;
    },[]);
   
    return ret;
}

